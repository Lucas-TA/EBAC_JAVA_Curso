package br.com.lascencao;

/**
 * Classe responsável por calcular a subtração entre dois números inteiros
 */
public class Subtracao {
	/**
	 * Função que recebe como parâmetro dois números <b>inteiros</b>
	 * @param a
	 * @param b
	 * @return retorna a subtração de a por b
	 */
	public int funcaoSubtrair(int a, int b) {
		return a - b;
	}
}
