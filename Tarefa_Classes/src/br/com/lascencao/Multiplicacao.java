package br.com.lascencao;

/**
 * Classe responsável por calcular a multiplicação entre dois números inteiros
 */
public class Multiplicacao {
	/**
	 * Função que recebe como parâmetro dois números <b>inteiros</b>
	 * @param a
	 * @param b
	 * @return retorna a multiplicação de a por b
	 */
	public int funcaoMultiplicar(int a, int b) {
		return a * b;
	}
}
