package br.com.lascencao;

/**
 * Classe responsável por calcular a soma entre dois números inteiros
 */
public class Soma {
	/**
	 * Função que recebe como parâmetro dois números <b>inteiros</b>
	 * @param a
	 * @param b
	 * @return retorna a soma de a e b
	 */
	public int funcaoSoma(int a, int b) {
		return a + b;
	}
}
