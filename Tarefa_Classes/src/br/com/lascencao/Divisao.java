package br.com.lascencao;

/**
 * Classe responsável por calcular a Divisão entre dois números inteiros
 */
public class Divisao {
	/**
	 * Função que recebe como parâmetro dois números <b>inteiros</b>
	 * @param a
	 * @param b
	 * @return retorna a divisão de a por b
	 */
	public int funcaoDividir(int a, int b) {
		return a / b;
	}
}
